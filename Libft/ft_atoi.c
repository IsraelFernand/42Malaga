#include "libft.h"

int ft_atoi(const char *str)
{
    int sign = 1;
    long result = 0;

    while (*str == ' ' || (*str >= 9 && *str <= 13))
        str++;
    if (*str == '-' || *str == '+')
    {
        if (*str == '-')
            sign = -1;
        str++;
    }
    while (*str >= '0' && *str <= '9')
    {
        result = result * 10 + (*str - '0');
        if (result * sign > 2147483647)
            return (-1);
        if (result * sign < -2147483648)
            return (0);
        str++;
    }
    return ((int)(result * sign));
}
